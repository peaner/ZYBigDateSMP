package cn.springmvc.model;

import java.io.Serializable;

import org.springframework.stereotype.Repository;

@Repository
public class DataDisplay implements Serializable {
    /**
	 * 
	 */
	private static final long serialVersionUID = 498170686379557439L;

	private String id;

    private String displayName;

    private String displaySorce;

    private String runType;

    private String displayPreview;
    
    private String path;

    private String remark;   

    private String updateUser;

    private String updateTime;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDisplayName() {
		return displayName;
	}

	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}

	public String getDisplaySorce() {
		return displaySorce;
	}

	public void setDisplaySorce(String displaySorce) {
		this.displaySorce = displaySorce;
	}

	public String getRunType() {
		return runType;
	}

	public void setRunType(String runType) {
		this.runType = runType;
	}

	public String getDisplayPreview() {
		return displayPreview;
	}

	public void setDisplayPreview(String displayPreview) {
		this.displayPreview = displayPreview;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getUpdateUser() {
		return updateUser;
	}

	public void setUpdateUser(String updateUser) {
		this.updateUser = updateUser;
	}

	public String getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}
  

   
}